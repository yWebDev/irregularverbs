'use strict';

/**
 * Writes src/app/build-info.prod.ts from package.json + git HEAD.
 * Run before `ng build --configuration=production` (see npm run build:prod).
 * Angular's compiler consumes TS before user esbuild plugins, so file replacement is reliable.
 */

const fs = require('node:fs');
const path = require('node:path');
const { execSync } = require('node:child_process');

const workspaceRoot = path.join(__dirname, '..', '..');
const pkg = JSON.parse(fs.readFileSync(path.join(workspaceRoot, 'package.json'), 'utf8'));
let gitCommit = 'unknown';
try {
  gitCommit = execSync('git rev-parse --short HEAD', {
    cwd: workspaceRoot,
    encoding: 'utf8',
  }).trim();
} catch {
  // shallow clone or no .git
}

const contents = `// Generated by tools/iverbs-builders/write-build-info.cjs — do not edit by hand
export const PACKAGE_VERSION = ${JSON.stringify(pkg.version)};
export const BUILD_TIME_ISO = ${JSON.stringify(new Date().toISOString())};
export const GIT_COMMIT = ${JSON.stringify(gitCommit)};
`;

const outPath = path.join(workspaceRoot, 'src', 'app', 'build-info.prod.ts');
fs.writeFileSync(outPath, contents, 'utf8');
console.log('Wrote', path.relative(workspaceRoot, outPath));
